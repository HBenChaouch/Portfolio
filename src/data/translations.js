const frozenFr = Object.freeze(fr);
const en = Object.freeze(Object.fromEntries(Object.keys(fr).map((key) => [key, key])));

export const dictionaries = Object.freeze({ fr: frozenFr, en });
export const languages = Object.freeze(["fr", "en"]);

const escapePattern = (value) => value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
const frenchPattern = new RegExp(
  Object.keys(frozenFr).sort((a, b) => b.length - a.length).map(escapePattern).join("|"),
  "g",
);

const financialTokenPattern = /(?:[~+>]?\s*)?(?:(?:€|\$)\s*\d+(?:[.,]\d+)?\s*(?:[mMBT])?|\d[\d ]*(?:[.,]\d+)?\s*(?:%|x|M€|Md€|Md\$)|€m|€|\$|%)/g;

function tokenizeFinancialValues(value) {
  const tokens = [];
  const template = value.replace(financialTokenPattern, (token) => {
    tokens.push(token);
    return "{{" + (tokens.length - 1) + "}}";
  });
  return { template, tokens };
}

export function translateText(value, language) {
  if (typeof value !== "string" || language === "en") return value;
  const { template, tokens } = tokenizeFinancialValues(value);
  const translated = template.replace(frenchPattern, (source) => dictionaries.fr[source]);
  if (translated === template) return value;
  return translated.replace(/\{\{(\d+)\}\}/g, (_, index) => tokens[Number(index)] ?? "");
}
